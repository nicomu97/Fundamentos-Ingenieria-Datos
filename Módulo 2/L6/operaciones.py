"""
Implementa las operaciones básicas de la calculadora.

Se implementan las siguientes funciones:
    - sumar
    - restar
    - multiplicar
    - dividir
"""

def sumar(a, b):
    "Suma dos números a y b."
    return a + b

def restar(a, b):
    "Resta dos números a y b."
    return a - b

def multiplicar(a, b):
    "Multiplica a y b."
    return a * b

def dividir(a, b):
    """Divide a entre b."""
    return a / b
