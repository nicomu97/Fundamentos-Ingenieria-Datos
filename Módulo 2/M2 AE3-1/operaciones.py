"""
Módulo que implementa operaciones aritméticas básicas.

Las funciones implementadas en este módulo son:
    - sumar
    - restar
    - multiplicar
    - dividir
"""

def sumar(a, b):
    """"Suma a y b."""
    return a + b

def restar(a, b):
    """Ejecuta la resta entre a y b."""
    return a - b

def multiplicar(a, b):
    """Multiplica a y b."""
    return a * b

def dividir(a, b):
    """Divide a entre b."""
    return a / b